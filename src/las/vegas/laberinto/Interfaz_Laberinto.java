    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        try {
            int n=Integer.parseInt(jTextField1.getText());
            if(n>=4 && n<=12){
                int width = n;
                int height = n;
                
                
                Laberinto_LasVegas mazeGenerator = new Laberinto_LasVegas(width, height);
                char[][] maze = mazeGenerator.generarLaberinto();

                jTextPane2.setText(mazeGenerator.printLaberintoOriginal());

                String solvedMazeText = mazeGenerator.printLaberintoSolucion();

                String formattedSolvedMazeText = solvedMazeText.replace("\n", "<br>");  // Reemplazo saltos de l√≠nea por <br> para HTML

// Agregar formato HTML para resaltar el texto en verde
                formattedSolvedMazeText = "<html>" + formattedSolvedMazeText + "</html>";
                formattedSolvedMazeText = formattedSolvedMazeText.replaceAll("\u001B\\[32m", "<font color='red'>");
                formattedSolvedMazeText = formattedSolvedMazeText.replaceAll("\u001B\\[0m", "</font>");

                jTextPane1.setContentType("text/html");
                jTextPane1.setText(formattedSolvedMazeText);
                
            }else{
                JOptionPane.showMessageDialog(this, "El numero de Filas y Columnas debe ser debe ser:\n1: Entero\n2: Mayor a 3\n3: Menor a 13");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un dato numerico entero.");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
